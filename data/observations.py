"""Clinical observation catalog.

Each ObservationDef holds the LOINC code, UCUM unit, and value-generation
parameters for a vital sign or lab result. The `normal_range` is used for
healthy patients; `abnormal_range` is used when a linked condition is active.
Thresholds drive the interpretation flag (H / L / N).
"""
